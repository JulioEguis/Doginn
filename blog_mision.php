                            <h2 class="pt-2 tm-color-primary tm-post-title">La misión de DOGiNN</h2>
                            <p class="tm-mb-40">16 de Mayo de 2024 by Julio Eguis</p>
                            <p style="color: black; font-weight: bold;">
                                ¿Cuál es la misión de DOGiNN?
                            </p>
                            <p>
                            La misión de Doginn es proporcionar un cuidado excepcional y amoroso para todas las 
                            mascotas que confían en nosotros, brindando un entorno seguro, cómodo y divertido 
                            donde puedan prosperar mientras sus dueños están fuera. Nos impulsa el compromiso 
                            de ser la opción preferida para aquellos que buscan alojamiento confiable y de 
                            calidad para sus mascotas, ofreciendo tranquilidad y confianza a nuestros clientes.
                            </p>
                            <p>
                            En nuestro enfoque hacia el cuidado de las mascotas, nos guían varios valores 
                            fundamentales:
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Bienestar animal
                            </p>
                            <p>
                            Nos comprometemos a asegurar el bienestar físico y emocional de todas las mascotas 
                            que se hospedan en nuestras instalaciones. Desde la alimentación adecuada hasta el 
                            ejercicio y la interacción social, nos aseguramos de que cada mascota reciba la 
                            atención individualizada que necesita para prosperar.
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Seguridad y comodidad
                            </p>
                            <p>
                            Valoramos la seguridad y la comodidad de las mascotas como prioridad absoluta. 
                            Nuestras instalaciones están diseñadas para proporcionar un entorno seguro y 
                            acogedor, con áreas de juego seguras y comodidades que imitan el entorno hogareño.
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Atención personalizada
                            </p>
                            <p>
                            Reconocemos que cada mascota es única, con necesidades y preferencias individuales. 
                            Nos comprometemos a proporcionar una atención personalizada, adaptada a las 
                            necesidades específicas de cada animal, desde la dieta hasta las actividades 
                            recreativas.
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Transparencia y comunicación
                            </p>
                            <p>
                            Fomentamos una comunicación abierta y transparente con nuestros clientes, 
                            manteniéndolos informados sobre el bienestar y el progreso de sus mascotas 
                            durante su estancia en DogInn. Nos esforzamos por establecer relaciones de 
                            confianza a largo plazo con cada cliente y su mascota.
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Compromiso con la excelencia
                            </p>
                            <p>
                            Buscamos la excelencia en todo lo que hacemos, desde la limpieza y el mantenimiento 
                            de nuestras instalaciones hasta la capacitación continua de nuestro personal. Nos 
                            esforzamos por superar las expectativas de nuestros clientes y ofrecer un servicio
                            excepcional en todo momento.
                            </p>
                            <span class="d-block text-right tm-color-primary">Objetivos · Confianza · Seguridad</span>
