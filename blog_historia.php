                            <h2 class="pt-2 tm-color-primary tm-post-title">La historia detras de DOGiNN</h2>
                            <p class="tm-mb-40">1 de Junio de 2024 by Sergio Seminario</p>
                            <p style="color: black; font-weight: bold;">
                                ¿Qué es DOGiNN?
                            </p>
                            <p>
                            Es un portal web comparador de hoteles para mascotas domésticas que tiene como objetivo 
                            ofrecer a los dueños una plataforma centralizada donde puedan encontrar y reservar fácilmente 
                            alojamiento para sus mascotas.
                            </p>
                            <p style="color: black; font-weight: bold;">
                                ¿Cómo nace DOGiNN?
                            </p>
                            <p>
                            Surge a partir de una experiencia personal compartida entre los dos socios durante una 
                            conversación en clase. Uno de los socios tenía la necesidad de encontrar alojamiento para 
                            su mascota cuando se ausentaba de casa debido a viajes u otras obligaciones. Al darse cuenta 
                            de lo complicado que resultaba encontrar información sobre hoteles para mascotas y gestionar 
                            reservas de forma eficiente, surgió la idea de crear un portal web que facilitara este proceso.
                            </p>
                            <p style="color: black; font-weight: bold;">
                                ¿A qué retos se enfrentaron los fundadores?
                            </p>
                            <p>
                            La inspiración de unos estudiantes de ASIR detrás del negocio surge de su pasión por la 
                            innovación y el aprendizaje constante. Estos emprendedores, comparten la visión de crear un 
                            portal web comparador de hoteles para perros. Este proyecto nace de su observación de una necesidad 
                            no cubierta en el mercado y de su determinación para transformar esa idea en realidad.
                            </p>
                            <p>
                            El principal desafío al que se enfrentaron al comenzar fue la superación de su limitado contacto 
                            previo con la informática. A pesar de provenir de trayectorias laborales muy diferentes, su experiencia
                            en tecnología no era muy amplia, al igual que la de muchos usuarios comunes. Esto significaba que tenían
                            que adquirir nuevas habilidades y conocimientos en el campo de la informática para poder llevar a cabo su
                            proyecto.
                            </p>
                            <p>
                            Tuvieron que sumergirse en un proceso de aprendizaje constante para familiarizarse con los aspectos 
                            técnicos y prácticos necesarios para desarrollar y lanzar su plataforma. Este desafío requería no sólo 
                            adquirir nuevos conocimientos, sino también aplicarlos de manera efectiva en la construcción de su negocio.
                            </p>
                            <p>
                            Sin embargo, a pesar de este desafío inicial, la determinación de estos estudiantes y su capacidad de adaptación
                            al utilizar las herramientas adquiridas en su formación, les permitieron superar los obstáculos y avanzar hacia la 
                            transformación de su idea en un negocio real.
                            </p>
