                            <h2 class="pt-2 tm-color-primary tm-post-title">El bienestar es fundamental</h2>
                            <p class="tm-mb-40">18 de Abril de 2024 by Sergio Seminario</p>
                            <p style="color: black; font-weight: bold;">
                                ¿De que manera nos comprometemos en DOGiNN?
                            </p>
                            <p>
                            En Doginn, nuestro compromiso con el bienestar y la felicidad de las mascotas 
                            es primordial. Trabajamos incansablemente para garantizar que cada mascota que 
                            se hospeda con nosotros experimente una estancia cómoda, segura y enriquecedora. 
                            <p>Aquí hay algunas maneras en las que cumplimos con este compromiso:</p>
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Estándares de calidad en los hoteles asociados
                            </p>
                            <p>
                            Nos asociamos con hoteles para mascotas que cumplen con rigurosos estándares de 
                            calidad y cuidado. Seleccionamos cuidadosamente estos hoteles en base a su reputación, 
                            instalaciones, y su compromiso con el bienestar animal.  Todos nuestros hoteles asociados 
                            deben cumplir con nuestros criterios estrictos antes de ser incluidos en nuestra plataforma.
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Medidas de seguridad
                            </p>
                            <p>
                            En Doginn, la seguridad de las mascotas es nuestra máxima prioridad. Todos nuestros hoteles 
                            asociados están equipados con medidas de seguridad para garantizar el bienestar de las mascotas 
                            durante su estancia. Esto puede incluir cercas seguras, sistemas de vigilancia, personal capacitado 
                            en primeros auxilios para mascotas, y protocolos de emergencia establecidos.
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Comodidades para una estancia confortable
                            </p>
                            <p>
                            Nos aseguramos de que nuestros hoteles asociados ofrezcan una amplia gama de comodidades para que 
                            las mascotas se sientan como en casa durante su estancia. Esto puede incluir áreas de juego espaciosas, 
                            camas cómodas, juguetes interactivos, y atención individualizada según las necesidades de cada mascota.
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Actividades recreativas y sociales
                            </p>
                            <p>
                            Fomentamos el enriquecimiento y la diversión de las mascotas durante su estancia en Doginn. 
                            Nuestros hoteles asociados ofrecen actividades recreativas supervisadas, como paseos, juegos 
                            y tiempo de juego con otros perros, para garantizar que las mascotas estén activas y felices 
                            durante su tiempo con nosotros.
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Atención personalizada y cariño
                            </p>
                            <p>
                            Cada mascota es única, y nos esforzamos por brindar una atención personalizada y cariñosa a 
                            cada una de ellas. Nuestro personal está capacitado para cuidar y mimar a las mascotas como 
                            si fueran propias, asegurándose de que se sientan amadas y seguras durante su estancia en nuestros 
                            hoteles asociados.
                            </p>
                            
