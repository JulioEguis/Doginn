                            <p class="tm-mb-40">2 de Abril de 2024 by Julio Eguis</p>
                            <p style="color: black; font-weight: bold;">
                                ¿Qué servicios ofrecen quienes trabajan con DOGiNN?
                            </p>
                            <p>
                            En DOGiNN, nos enorgullece ofrecer una amplia gama de servicios diseñados para satisfacer 
                            las necesidades individuales de cada cliente y su mascota. Nuestro objetivo es proporcionar 
                            una experiencia excepcional y personalizada que garantice la comodidad, la seguridad y la 
                            felicidad de todas las mascotas que se hospedan con nosotros.
                            <p>Aquí están algunos de los servicios específicos que ofrecemos:</p>
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Opciones de alojamiento
                            </p>
                            <p>
                            Ofrecemos una variedad de opciones de alojamiento para adaptarnos a las preferencias y 
                            necesidades de cada mascota. Esto puede incluir habitaciones individuales, suites de lujo 
                            o áreas comunes para la socialización, según las preferencias del cliente y la personalidad 
                            de la mascota.
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Actividades recreativas
                            </p>
                            <p>
                            Nuestros hoteles asociados ofrecen una amplia gama de actividades recreativas supervisadas 
                            para mantener a las mascotas activas y entretenidas durante su estancia. Esto puede incluir 
                            paseos diarios, tiempo de juego en áreas seguras y juegos interactivos con otros perros.
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Cuidado especializado
                            </p>
                            <p>
                            Entendemos que algunas mascotas pueden tener necesidades especiales o requerir cuidados 
                            adicionales durante su estancia. Por ello, ofrecemos cuidado especializado según las 
                            necesidades individuales de cada mascota, que puede incluir administración de medicamentos, 
                            dietas especiales, atención veterinaria o cuidados específicos según las indicaciones del cliente.
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Servicios de grooming
                            </p>
                            <p>
                            Muchos de nuestros hoteles asociados ofrecen servicios de grooming para garantizar que las 
                            mascotas se vean y se sientan lo mejor posible durante su estancia. Esto puede incluir baños, 
                            cepillado, corte de uñas y otros tratamientos de belleza según las necesidades de cada mascota.
                            </p>
                            <p style="color: black; font-weight: bold;">
                            Atención personalizada y cariño
                            </p>
                            <p>
                            Nuestro personal está capacitado para proporcionar una atención personalizada y amorosa a 
                            cada mascota que se hospeda con nosotros. Nos esforzamos por mimar a las mascotas como si 
                            fueran nuestras propias, asegurándonos de que se sientan amadas, seguras y felices durante 
                            su estancia en nuestros hoteles asociados.
                            </p>
                           
